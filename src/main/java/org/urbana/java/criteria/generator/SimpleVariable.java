package org.urbana.java.criteria.generator;

import java.util.ArrayList;
import java.util.List;

public class SimpleVariable  {
	
	private List<String> variableName = new ArrayList<String>();;
	private Double consFactor;
	
	public List<String> getVariableName() {
		return variableName;
	}
	public void setVariableName(List<String> variableName) {
		this.variableName = variableName;
	}
	public Double getConsFactor() {
		return consFactor;
	}
	public void setConsFactor(Double consFactor) {
		this.consFactor = consFactor;
	}
	
	
	
	
	
	
}
